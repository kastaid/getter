@kasta_cmd(
    pattern="(owo|shg)$",
    no_crash=True,
)
async def _(kst):
    match = kst.pattern_match.group(1)
    if match == "owo":
        chars = UWUS
    elif match == "shg":
        chars = SHRUGS
    text = choice(chars)
    await kst.eor(text)


